int robotSim(vector<int> &commands, vector<vector<int>> &obstacles) {
    int dx[] = {0, 1, 0, -1};
    int dy[] = {1, 0, -1, 0};
    set<pair<int, int>> S;
    for (auto obstacle : obstacles) {
        S.insert(make_pair(obstacle[0], obstacle[1]));
    }
    int x = 0, y = 0, dire = 0, ans = 0;

    for (int command : commands) {
        if (command == -1) {
            dire = (dire + 1) % 4;
        } else if (command == -2) {
            dire = (dire + 3) % 4;
        } else {
            for (int i = 0; i < command; i++) {
                int x1 = x + dx[dire];
                int y1 = y + dy[dire];
                if (S.find(make_pair(x1, y1)) != S.end()) break;
                x = x1;
                y = y1;
                ans = max(ans, x * x + y * y);
            }
        }
    }

    return ans;
}

